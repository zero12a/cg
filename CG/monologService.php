class monologService 
{
	private $DAO;
	private $DB;
	//생성자
	function __construct(){
		alog("MonologService-__construct");

		$this->DAO = new monologDao();
	    //$this->DB = db_s_open();
		$this->DB["CG"] = db_obj_open(getDbSvrInfo("CG"));
	}
	//파괴자
	function __destruct(){
		alog("MonologService-__destruct");

		unset($this->DAO);
		if($this->DB["CG"])$this->DB["CG"]->close();
		unset($this->DB);
	}
	function __toString(){
		alog("MonologService-__toString");
	}
	//, 조회(전체)
	public function goG1Searchall(){
