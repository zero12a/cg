		//그리드 서버 조회 
		//GRID_SEARCH____________________________start
		$GRID["SQL"] = array();
		$GRID["KEYCOLIDX"] = -1; // KEY 컬럼, 

		//조회
	//암호화컬럼
		$GRID["COLCRYPT"] = array();
		//필수 여부 검사
